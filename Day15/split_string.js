// Split() Method:

// it is used to split a string into an array of substring, and returns the new array.
// Delimeter for the split function. It can be a space or any character and js will look for that inside the string and split.

var str = "It will display the array of the size";

console.log(typeof str);

var arr = str.split(" "); // ['It', 'will', 'display', 'the', 'array', 'of', 'the', 'size']

console.log(arr);
console.log(typeof arr);