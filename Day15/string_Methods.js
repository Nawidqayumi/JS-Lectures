// toLowerCase() 

var str1 = "ET-TEST EDU";

console.log(`toLowercase:  ${str1.toLowerCase()}`);

// toUpperCase()
var str2 = "et-test edu";

console.log(`toUpperCase: ${str2.toUpperCase()}`);

// trim() method: removes white spaces from both ends of a string

var str3 = "      Et-test Edu       ";

console.log(`without trim method: ${str3}  length: ${str3.length}`);
console.log(`with trim method: ${str3.trim()} length: ${str3.trim().length} `);