// switch statements :
// switch case statement is used to compare the value of a variable with multiple values and execute some statements based
// on the match.

var username = "Khalmurch";

switch (username) {
  case "Ahmad":
    console.log("Ahmad is here");
    break;
  case "Mahmud":
    console.log("Mahmud is here");
    break;
  case "Khalmurcha":
    console.log("Khalmurcha is here");
    break;
  default:
    console.log("no one is here");
    break;
}

