// Comparison Operators:
// returns always boolean value (true or false)

// 1- Equality   =>      ==
var x  = 1;
var y  = 1;
var z  = 2;

console.log(`x==y :  ${x==y}`);
console.log(`x==z :  ${x==z}`);

// 2- Inequality =>   !=

console.log(`x!=y :  ${x!=y}`);
console.log(`x!=z :  ${x!=z}`);

// 3- Identity/ Strict Equality =>     ===
var a = "1";
console.log(`x===y :  ${x===y}`);
console.log(`x==a :  ${x==a}`);
console.log(`x===a :  ${x===a}`);

// 4- Non-identity/ Strict Inequality =>   !==
console.log(`x!==y :  ${x!==y}`);
console.log(`x!=a :  ${x!=a}`);
console.log(`x!==a :  ${x!==a}`);

// 5- Greater than =>      >

console.log(`z>y : ${z>y}`);

// 6- Greater than or equal =>    >=

console.log(`z>=y : ${z>=y}`);
console.log(`x>=y : ${x>=y}`);

// 7- Less than =>      <

console.log(`z<y : ${z<y}`);

// 8- Less than or equal =>     <=

console.log(`z<=y : ${z<=y}`);
console.log(`x<=y : ${x<=y}`);