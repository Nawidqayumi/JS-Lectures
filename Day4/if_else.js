// if... else statements
// check the condition. if it resolves to true the first code block is executed. 
// if the condition resolve to false, the second code block is run instead.

// structure

var score = 40;

if (score>=50){
    console.log(`Congratulations, you are passed and your score is ${score}`);
} else {
    console.log(`Sorry, you are failed and your score is ${score}`);
}