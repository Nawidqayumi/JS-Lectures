// sort() method:
// the sort() method sorts the elements of an array in place and returns the array.

// Ascending orders

var months = ["May", "April", "February", "December", "August"];
months.sort();
console.log(months);

var array1 = [200,1,20,4,5,90];
array1.sort();
console.log(array1);

var mixArrays = ["cat", "dog", 78, "Beer"];
mixArrays.sort();
console.log(mixArrays);
